// Type exports for theme usage
export type Theme = typeof theme
export type ThemeColors = typeof theme.colors
export type ThemeTypography = typeof theme.typography
export type ThemeSpacing = typeof theme.spacing

// Utility functions for theme usage
export const getThemeColor = (colorPath: string): string => {
  const path = colorPath.split('.')
  let value: any = theme.colors
  
  for (const key of path) {
    if (value && typeof value === 'object' && key in value) {
      value = value[key]
    } else {
      console.warn(`Color path "${colorPath}" not found in theme`)
      return theme.colors.primary[500] // fallback
    }
  }
  
  return value
}

export const getThemeSpacing = (size: keyof ThemeSpacing): string => {
  return theme.spacing[size]
}

export const getThemeFontSize = (size: keyof ThemeTypography['fontSize']): string => {
  return theme.typography.fontSize[size][0]
}

export const getThemeFontWeight = (weight: keyof ThemeTypography['fontWeight']): string => {
  return theme.typography.fontWeight[weight]
}
